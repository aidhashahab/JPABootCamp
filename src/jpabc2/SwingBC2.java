    private List<Product> getProduct() {
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("Select p From Product p");
        List<Product> produks = q.getResultList();
        em.close();
        return produks;
    }

    private void showToTableProduct() {

        List<Product> produks = getProduct();
        DefaultTableModel tableModel = (DefaultTableModel) produkListTable.getModel();

        Object[] row = new Object[3];
        for (Product p : produks) {
            row[0] = p.getNamaProduk();
            row[1] = p.getHargaProduk();
            row[2] = p.getQuantity();
            
            tableModel.addRow(row);
            
        }
    }

